package com.edu.bookrental;

public class ToStringTest {

	public static void main(String[] args) {
		System.out.println("\t"+"-------------------------------------------------------------\n"
							+ "\t카테고리" + "\t제목" + "\t\t저자" + "\t  출판사" + "\t\t수량\n"
							+ "\t"+"-------------------------------------------------------------\n");
		
		System.out.printf("\t"+"-------------------------------------------------------------\n"
				+ "\t%-8s" + "%-18s" + "%-10s" + "%-10s" + "%-2s\n"
				+ "\t"+"-------------------------------------------------------------", "카테고리", "제목", "저자", "출판사", "수량");
		
	}
}
